    private void jButtonIniciarSesionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonIniciarSesionActionPerformed
        String usuario=jTextFieldUsuario.getText();
        char contrasenaArray[]=jPasswordFieldContrasena.getPassword(); //Obtenemos la contraseña como array de chars
        String contrasena=contrasenaArray.toString(); //Pasamos la contraseña a String
        boolean loginValido=true; //Variable para saber si el login es valido o no
        if(contieneNumeros(usuario)){ //Si el usuario contiene numeros
            JOptionPane.showMessageDialog(this, "El usuario no puede contener numeros","ERROR LOGIN",JOptionPane.ERROR_MESSAGE); //Lanzamos un mensjae de error
            loginValido=false; //El login no es valido
        }
        if(loginValido){ //Si los datos son validos
            ParametrosInicio parametros=new ParametrosInicio(this,true); //Creamos un Dialog de parametros
            parametros.setVisible(true); //Lo hacemos visible
        }
    }//GEN-LAST:event_jButtonIniciarSesionActionPerformed

    private void jLabelMostrarContrasenaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabelMostrarContrasenaMouseClicked
        if(jPasswordFieldContrasena.getEchoChar()==(char)0) jPasswordFieldContrasena.setEchoChar('*'); //Si la password es visible, la ocultamos con *
        else jPasswordFieldContrasena.setEchoChar((char)0); //Si la password no es visible, la hacemos visible
    }//GEN-LAST:event_jLabelMostrarContrasenaMouseClicked

    private void validationPanelStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_validationPanelStateChanged
        if(validationPanel.getProblem()==null)jButtonIniciarSesion.setEnabled(true); //Si no hay problemas, activamos el boton
        else jButtonIniciarSesion.setEnabled(false); //Si hay problemas, lo desactivamos
    }//GEN-LAST:event_validationPanelStateChanged

    private static boolean contieneNumeros(String s){
        return s.matches(".*\\d+.*"); //Devuelve true si contiene digitos, false si no
    }
