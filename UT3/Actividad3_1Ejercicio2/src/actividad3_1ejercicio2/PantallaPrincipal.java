    private void jButtonRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRegistrarActionPerformed
        //Obtenemos los valores introducidos y los guardamos en variables
        String nombre=jTextFieldNombre.getText();
        String direccion=jTextFieldDireccion.getText();
        String pais=jComboBoxPais.getSelectedItem().toString();
        String tipoCliente=jComboBoxTipoCliente.getSelectedItem().toString();
        
        //Comprobamos los campos que puede modificar el usuario (los TextField)
        String mensaje=""; //Mensaje a mostrar en el JOptionPane posterior
        boolean insertar=true; //Variable de paso que decide si insertamos o no
        if(nombre.equals("")){ //Si el nombre está vacío
            mensaje+="El nombre del cliente no puede estar vacío.\n"; //Se advierte al usuario
            insertar=false; //No insertamos
        } 
        if(direccion.equals("")) { //Si la dirección está vacía
            mensaje+="La direccion del cliente no puede estar vacia.\n"; //Se advierte al usuario
            insertar=false; //No insertamos
        } 
        //COmprobamos si se inserta o no
        if (insertar){ //Si los datos son correctos
            mensaje=nombre+", de "+pais+", con direccion "+direccion+" y de tipo "+tipoCliente+" ha sido registrado.\n"; //Damos forma al mensaje con los datos
            if(jCheckBoxSuscrito.isSelected())mensaje+="Se ha suscrito al boletín de novedades"; //Si el checkBox está marcado, lo añadimos al mensaje
            JOptionPane.showMessageDialog(this,mensaje,"Inserción realizada",JOptionPane.INFORMATION_MESSAGE);
        }
        else { //Si los datos no son correctos
            mensaje+="Cliente "+nombre+" no insertado"; //Avisamos al usuario del error en el mensaje
            JOptionPane.showMessageDialog(this,mensaje,"Error en la inserción",JOptionPane.ERROR_MESSAGE); //Mostramos un mensaje de error con el texto creado
        } 
        
    }//GEN-LAST:event_jButtonRegistrarActionPerformed

