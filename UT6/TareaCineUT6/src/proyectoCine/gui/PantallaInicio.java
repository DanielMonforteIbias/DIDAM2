    private void jButtonAccederActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAccederActionPerformed
        String usuario = jTextFieldUsuario.getText().trim();
        char[] contraseñaArray = jPasswordFieldContraseña.getPassword();
        String contraseña = new String(contraseñaArray);
        boolean loginValido = true;
        if (usuario.isEmpty() || contraseña.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Los campos usuario y contraseña no pueden estar vacíos", "Error", JOptionPane.ERROR_MESSAGE);
            loginValido = false;
        }
        if (loginValido) {
            if (verificarConexion()) {
                jTextFieldUsuario.setText("");
                jPasswordFieldContraseña.setText("");
                JOptionPane.showMessageDialog(rootPane, "Bienvenido, " + usuario + "!", "Bienvenido", JOptionPane.INFORMATION_MESSAGE);
                Principal principal = new Principal();
                principal.setVisible(true);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(rootPane, "Error de conexión con la base de datos", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButtonAccederActionPerformed
    private boolean verificarConexion() {
        String url = "jdbc:mysql://localhost:3306/peliculas";
        String user = "root";
        String password = "";

        try (Connection conn = DriverManager.getConnection(url, user, password)) {
            return conn != null;
        } catch (SQLException e) {
            return false;
        }
    }
