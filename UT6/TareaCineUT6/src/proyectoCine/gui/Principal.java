    private void jButtonGuardarPeliculaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGuardarPeliculaActionPerformed
        String tituloPelicula = jTextFieldTituloPelicula.getText().trim(); //Obtenemos el titulo
        if (tituloPelicula.isEmpty()) { //Si esta vacio,  daremos un error
            JOptionPane.showMessageDialog(this, "El título no puede estar vacío", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if(PeliculaDao.obtenerPeliculaPorTitulo(tituloPelicula)!=null){ //Si ya existe una peli con ese nombre,  daremos un error
            JOptionPane.showMessageDialog(this, "Ya existe una pelicula con ese nombre", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (!jRadioButtonSi.isSelected() && !jRadioButtonNo.isSelected()) { //Si no esta seleccionado si se ha visto o no,  daremos un error
            JOptionPane.showMessageDialog(this, "Debe seleccionar si la ha visto", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        //Obtenemos genero y cine
        String nombreGenero = (String) jComboBoxGeneros.getSelectedItem();
        String nombreCine = (String) jComboBoxCines.getSelectedItem();
        //Si alguno no está seleccionado, daremos error
        if (nombreGenero == null || nombreCine == null) {
            JOptionPane.showMessageDialog(this, "Debe seleccionar un género y un cine", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Validamos el  campo de calificación si selecciona que la ha visto tiene que calificarla si o si
        boolean visto = jRadioButtonSi.isSelected();
        int calificacion = 0;
        if (visto) {
            String calificacionText = jTextFieldCal.getText().trim();
            if (calificacionText.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Debe ingresar una calificación si ha visto la película", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            try {
                calificacion = Integer.parseInt(calificacionText); //Vemios si es un numero valido
                if (calificacion < 1 || calificacion > 5) { //Ademas vemos que este entre 1 y 5
                    JOptionPane.showMessageDialog(this, "La calificación debe estar entre 1 y 5", "Error", JOptionPane.ERROR_MESSAGE);
                    return;
                }
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "La calificación debe ser un número válido", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
        }
        //Obtenemos el genero y cine por sus nombres
        Genero genero = GeneroDao.obtenerGeneroPorNombre(nombreGenero);
        Cine cine = CineDao.obtenerCinePorNombre(nombreCine);
        Pelicula pelicula = new Pelicula(tituloPelicula, genero, visto, cine, calificacion); //Creamos la pelicula
        PeliculaDao.registrarPelicula(pelicula); //La registramos en la base de datos
        refrescarTablaPeliculas(PeliculaDao.filtrarPeliculas(filtroGenero, filtroCine)); //Refrescamos la tabla para que salga
        jComboBoxTitulo.addItem(pelicula.getNombrePelicula()); //Añadimos su titulo al comboBox de peliculas
        JOptionPane.showMessageDialog(this, "Película guardada", "Mensaje", JOptionPane.INFORMATION_MESSAGE); //Avisamos al usuario
        //Vaciamos los campos
        jTextFieldTituloPelicula.setText("");
        jComboBoxCines.setSelectedIndex(0);
        jComboBoxGeneros.setSelectedIndex(0);
        jTextFieldCal.setText("");
    }//GEN-LAST:event_jButtonGuardarPeliculaActionPerformed

    private void jButtonGuardarCineActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGuardarCineActionPerformed
        //Obtenemos los datos del cine
        String nombreCine = jTextFieldNombreCine.getText().trim();
        String direccionCine = jTextFieldDireccionCine.getText().trim();
        Cine cine;
        if (nombreCine.isEmpty() || direccionCine.isEmpty()) { //Los dos campos deben estar rellenos
            JOptionPane.showMessageDialog(rootPane, "Los campos nombre y dirección no pueden estar vacíos", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            if (CineDao.comprobarCine(nombreCine)) { //Comprobamos que no haya un cine con el mismo nombre
                JOptionPane.showMessageDialog(rootPane, "El cine ya existe", "Error", JOptionPane.ERROR_MESSAGE);
            } else {
                cine = new Cine(); //Creamos el cine y le ponemos los datos
                cine.setNombreCine(nombreCine);
                cine.setDireccionCine(direccionCine);
                CineDao.registrarCine(cine); //Lo registramos en la BD
                rellenarComboBoxCine(); //Actualizamos el comboBox de cines
                JOptionPane.showMessageDialog(rootPane, cine.getNombreCine() + " guardado", "Mensaje", JOptionPane.INFORMATION_MESSAGE); //Avisamos al usuario
                //Vaciamos los campos
                jTextFieldNombreCine.setText("");
                jTextFieldDireccionCine.setText("");
            }
        }
    }//GEN-LAST:event_jButtonGuardarCineActionPerformed

