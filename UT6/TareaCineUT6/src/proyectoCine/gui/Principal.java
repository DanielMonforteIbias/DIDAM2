    private void refrescarTablaPeliculas(List<Pelicula> listaPeliculas) {
        DefaultTableModel dtm = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false; //Hacemos que las celdas no sean editables al hacer doble clic sobre ellas
            }
        };
        dtm.setColumnIdentifiers(new String[]{"Titulo", "Genero", "Vista", "Cine", "Calificacion"}); //Ponemos nombre a la columna
        for (Pelicula p : listaPeliculas) { //Para cada pelicula de la lista
            String[] fila = new String[5]; //Creamos un Array de Strings de longitud 5
            fila[0] = p.getNombrePelicula();
            fila[1] = p.getGenero().getNombreGenero();
            if (p.isVisto()) {
                fila[2] = "Si";
            } else {
                fila[2] = "No";
            }
            fila[3] = p.getCine().getNombreCine();
            fila[4] = p.getCalificacion() + "";
            dtm.addRow(fila); //Añadimos el array a la tabla
        }
        jTablePeliculas.setModel(dtm); //Establecemos el modelo creado como el de la tabla
        
        jScrollPane1.getViewport().setBackground(new Color(0xFDF5E8)); //Establecemos el color de fondo del jScrollPane que contiene la tabla

        //Apartado visual de la tabla
        DefaultTableCellRenderer headerRenderer = new DefaultTableCellRenderer();
        headerRenderer.setBackground(new Color(0xC8B18B)); //Cambiamos el fondo del header
        headerRenderer.setForeground(Color.BLACK); //Cambiamos el color de fuente del header
        headerRenderer.setHorizontalAlignment(JLabel.CENTER); //Centramos el texto del header
        for (int i = 0; i < jTablePeliculas.getModel().getColumnCount(); i++) { //Para cada columna
            jTablePeliculas.getColumnModel().getColumn(i).setHeaderRenderer(headerRenderer); //Establecemos el header al que hemos creado
        }
    }

