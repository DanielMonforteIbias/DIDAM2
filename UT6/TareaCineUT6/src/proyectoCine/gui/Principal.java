    private void jButtonImprimirInformeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonImprimirInformeActionPerformed
        try {
            Class.forName("com.mysql.jdbc.Driver").newInstance();
            Connection connection = DriverManager.getConnection("jdbc:mysql://localhost/peliculas", "root", ""); //Obtenemos conexion a la base de datos
            JasperPrint print = JasperFillManager.fillReport("reports/report_cines.jasper", null, connection);

            String formato = jComboBoxFormatos.getSelectedItem().toString().toLowerCase(); //Obtenemos el formato deseado
            String outputPath = "reports/informeCines." + formato;
            //Segun el formato generaremos un informe u otro de diferentes maneras
            switch (formato) {
                case "pdf":
                JasperExportManager.exportReportToPdfFile(print, outputPath);
                break;

                case "csv":
                SimpleCsvExporterConfiguration csvConfig = new SimpleCsvExporterConfiguration();
                csvConfig.setFieldDelimiter(";");
                new JRCsvExporter() {
                    {
                        setExporterInput(new SimpleExporterInput(print));
                        setExporterOutput(new SimpleWriterExporterOutput(outputPath));
                        setConfiguration(csvConfig);
                        exportReport();
                    }
                };
                break;

                case "xlsx":
                SimpleXlsxReportConfiguration xlsxConfig = new SimpleXlsxReportConfiguration();
                xlsxConfig.setDetectCellType(true);
                xlsxConfig.setOnePagePerSheet(false);
                new JRXlsxExporter() {
                    {
                        setExporterInput(new SimpleExporterInput(print));
                        setExporterOutput(new SimpleOutputStreamExporterOutput(outputPath));
                        setConfiguration(xlsxConfig);
                        exportReport();
                    }
                };
                break;
                case "xml":
                    JasperExportManager.exportReportToXmlFile(print, outputPath, true);
                    break;
            }
            JOptionPane.showMessageDialog(this, "Informe generado en la carpeta reports: " + outputPath, "AVISO", JOptionPane.INFORMATION_MESSAGE); //Avisamos al usuario para que sepa que se genero
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error al generar el informe: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE); //Avisamos al usuario del error
        }
    }//GEN-LAST:event_jButtonImprimirInformeActionPerformed

    private void jButtonEliminarPeliculaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEliminarPeliculaActionPerformed
        Pelicula p = PeliculaDao.obtenerPeliculaPorTitulo(jComboBoxTitulo.getSelectedItem().toString()); //Obtenemos la pelicula seleccionada
        int confirm = JOptionPane.showConfirmDialog(this, "¿Desea eliminar la película " + p.getNombrePelicula() + "?", "Confirmación", JOptionPane.YES_NO_OPTION); //Damos al usuario la oportunidad de decidir si borrarla o no
        if (confirm == JOptionPane.YES_OPTION) { //Si decide que si
            PeliculaDao.eliminarPelicula(p.getNombrePelicula()); //La eliminamos
            refrescarTablaPeliculas(PeliculaDao.filtrarPeliculas(filtroGenero, filtroCine)); //Refrescamos la tabla para quitarla
            jComboBoxTitulo.removeItem(p.getNombrePelicula()); //La quitamos del comboBox de peliculas
            JOptionPane.showMessageDialog(this, p.getNombrePelicula()+" eliminada", "Eliminada", JOptionPane.INFORMATION_MESSAGE); //Informamos al usuario
        }
    }//GEN-LAST:event_jButtonEliminarPeliculaActionPerformed

    private void jButtonModificarPeliculaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonModificarPeliculaActionPerformed
        Pelicula pelicula = PeliculaDao.obtenerPeliculaPorTitulo(jComboBoxTitulo.getSelectedItem().toString()); //Obtenemos la pelicula seleccionada
        if (pelicula != null) {
            //Obtenemos los datos ingresados
            Genero genero=GeneroDao.obtenerGeneroPorNombre(jComboBoxGeneros1.getSelectedItem().toString());
            boolean vista=false;
            if(jRadioButtonSi1.isSelected())vista=true;
            else if(jRadioButtonNo1.isSelected())vista=false;
            Cine cine=CineDao.obtenerCinePorNombre(jComboBoxCines1.getSelectedItem().toString());
            String calificacionText = jTextFieldCal1.getText().trim();
            int calificacion=0;
            if(vista){
                if (calificacionText.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Debe ingresar una calificación si ha visto la película", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            try {
                calificacion = Integer.parseInt(calificacionText);
                if (calificacion < 1 || calificacion > 5) {
                    JOptionPane.showMessageDialog(this, "La calificación debe estar entre 1 y 5", "Error", JOptionPane.ERROR_MESSAGE);
                    return;
                }
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "La calificación debe ser un número válido", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            }
            PeliculaDao.modificarPelicula(new Pelicula(pelicula.getNombrePelicula(),genero,vista,cine,calificacion)); //Modificamos la pelicula con los nuevos datos
            JOptionPane.showMessageDialog(this, pelicula.getNombrePelicula()+" modificada", "Modificada", JOptionPane.INFORMATION_MESSAGE); //Avisamos al usuario
            refrescarTablaPeliculas(PeliculaDao.filtrarPeliculas(filtroGenero, filtroCine)); //Refrescamos la tabla para que salga con los cambios
        }
    }//GEN-LAST:event_jButtonModificarPeliculaActionPerformed

